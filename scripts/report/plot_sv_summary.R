	org.Hs.eg.db,
	keys = transcript_table$TXNAME,
	keytype = 'UCSCKG',
	column = 'SYMBOL'
	);

anno.data <- merge(
	aggregate(TXSTART ~ ENTREZID + SYMBOL + TXCHROM, transcript_table, min),
	aggregate(TXEND ~ ENTREZID + SYMBOL + TXCHROM, transcript_table, max)
	);
