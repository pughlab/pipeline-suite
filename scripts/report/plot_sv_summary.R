# determine SV status (germline/somatic)
sv.data$Status <- apply(
	sv.data[,grep('normal_genome|normal_transcriptome', colnames(sv.data))],
	1, function(i) {
		if (any(grepl('germline|Not Applicable', i))) {
			return('germline') } else { return('somatic') }
		}
